/**
 * 将翻译指令追加到 OCR prompt 末尾
 * @param {string} basePrompt - 基础 prompt
 * @param {string} lang - 目标语言，如 '中文'
 * @returns {string} 拼接后的 prompt
 */
export const appendTranslateInstruction = (basePrompt, lang) => {
  if (!lang || !lang.trim()) return basePrompt;
  return `${basePrompt}\n\n请在完成文字识别后，将识别结果翻译成${lang.trim()}，只输出翻译后的内容。`;
};
